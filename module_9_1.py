def apply_all_func(int_list, *functions):
    """
    Применяет переданные функции к списку чисел и возвращает результаты в виде словаря.

    Параметры:
    int_list (list): Список чисел (int, float).
    *functions: Неограниченное количество функций, которые применимы к спискам чисел.

    Возвращает:
    dict: Словарь, где ключом является название функции, а значением - результат её работы с int_list.
    """
    # Создаем пустой словарь для хранения результатов
    results = {}
    
    # Перебираем все переданные функции
    for func in functions:
        # Применяем функцию к списку и сохраняем результат в словарь под ключом - названием функции
        results[func.__name__] = func(int_list)
    
    # Возвращаем словарь с результатами
    return results

# Пример использования функции
if __name__ == "__main__":
    # Пример списка чисел
    int_list = [6, 20, 15, 9]
    
    # Пример вызова функции с разными наборами функций
    print(apply_all_func(int_list, max, min))
    print(apply_all_func(int_list, len, sum, sorted))